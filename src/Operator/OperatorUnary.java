package Operator;

public class OperatorUnary {
    public static void main(String[] args) {
        int i = 0;
        i++;
        System.out.println(i);
        i++;
        System.out.println(i);
        i++;
        System.out.println(i);

        //operator lain

        i+=3; // sama dengan i=i+3
        System.out.println(i);

        i+=3;
        System.out.println(i);

        i*=3; //sama dengan i = i*3
        System.out.println(i);

        i*=3;
        System.out.println(i);




    }
}

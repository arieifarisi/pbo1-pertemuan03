package Operator;

public class OperatorEquality {
    public static void main(String[] args) {
        int x = 1;
        int y = 2;
        System.out.println(x==1);
        System.out.println(x==6);
        System.out.println(x==y);
        System.out.println(x>y);
        System.out.println(x<y);
        System.out.println(1>2);
        System.out.println(1<2);
        System.out.println(x!=1);
        System.out.println(y!=1);
    }
}

package percabangan;

public class Switch {
    public static void main(String[] args) {
        int jalanJalan = 3;
        switch (jalanJalan){
            case 1:
                System.out.println("Orang Lapas");
                break;
            case 2:
                System.out.println("Double Date");
                break;
            case 3:
                System.out.println("Obat Nyamuk");
                break;
            case 4:
                System.out.println("Orang Pacaran");
                break;
            default:
                System.out.println("Hantu");
                break;
        }
    }
}
